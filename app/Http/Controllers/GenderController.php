     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required',
        ]);

        $gender = new Gender([
            'name' => $request->get('name'),
        ]);
        $gender->save();
        return redirect('/gender')->with('success', 'Gender saved!');
    }
